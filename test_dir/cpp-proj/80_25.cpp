
























#include <stdio.h>
#include <errno.h>
#include <string.h>
int main(void)
{
    char *fileName = "e.c";
    // printf("start: errno = %d\n", errno);
    if (fopen(fileName, "r") == NULL)
    {
        /*
        Converts an error code to the corresponding error message
        */
        // perror(fileName);
        printf("errno %d: %s\n", errno, strerror(errno));
    }
    // printf("end: errno = %d\n", errno);
    // printf("end: errno = %d\n", errno);
    // printf("end: errno = %d\n", errno);
    return 0;
}
